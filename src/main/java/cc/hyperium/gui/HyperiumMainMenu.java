    private DynamicTexture getCachedTexture(String t) {
        final DynamicTexture[] texture = {this.cachedImages.get(t)};
        if (texture[0] == null) {
            Minecraft.getMinecraft().addScheduledTask(() -> {
                try {
                    texture[0] = new DynamicTexture(ImageIO
                        .read(new URL("https://crafatar.com/avatars/" + t
                            + "?size=30?default=MHF_Steve&overlay")));

                } catch (Exception ignored) {
                    try {
                        texture[0] = new DynamicTexture(ImageIO
                            .read(new URL(
                                "https://crafatar.com/avatars/c06f89064c8a49119c29ea1dbd1aab82")));
                    } catch (IOException e) {
                        e.printStackTrace();
                    }
                }
                this.cachedImages.put(t, texture[0]);
            });

        }
        return texture[0];
    }

